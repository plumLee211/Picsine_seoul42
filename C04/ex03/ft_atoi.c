#include <unistd.h>

int		ft_atoi(char *str)
{
	int i;
	int path;
	int sign;

	path = 0;
	i = 0;
	sign = 1;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\v' ||
			str[i] == '\n' || str[i] == '\r' || str[i] == '\f')
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		path = path * 10 + str[i] - '0';
		i++;
	}
	return (sign * path);
}
